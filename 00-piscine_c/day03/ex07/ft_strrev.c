char	*ft_strrev(char *str)
{
	char	*p;
	char	*str_copy;
	char	c;

	p = str;
	str_copy = str;
	while (*p != '\0')
		p++;
	p--;
	while (((p - str) > 0))
	{
		c = *str;
		*str = *p;
		*p = c;
		p--;
		str++;
	}
	return (str_copy);
}
