int		ft_strlen(char *str);

int		ft_valid_line(char *str)
{
	int i;

	i = -1;
	if (ft_strlen(str) != 9)
		return (0);
	while (str[++i])
	{
		if ((str[i] < '1' || str[i] > '9') && str[i] != '.')
			return (0);
	}
	return (1);
}
