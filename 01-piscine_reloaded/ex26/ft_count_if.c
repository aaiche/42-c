int	ft_count_if(char **tab, int (*f)(char*))
{
	int	i;
	int	rc;

	rc = 0;
	i = 0;
	while (tab[i] != 0)
	{
		rc += (*f)(tab[i]);
		i++;
	}
	return (rc);
}
