int	ft_isalpha(int c)
{
	int	rc;

	rc = 0;
	if (((c >= 'A') && (c <= 'Z')) || ((c >= 'a') && (c <= 'z')))
	{
		rc = 1;
	}
	else
	{
		rc = 0;
	}
	return (rc);
}
